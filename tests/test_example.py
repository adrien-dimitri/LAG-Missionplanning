# test_example.py

def test_addition():
    # Test simple addition
    result = 2 + 3
    assert result == 5  # Check if 2 + 3 equals 5

def test_subtraction():
    # Test simple subtraction
    result = 10 - 4
    assert result == 6  # Check if 10 - 4 equals 6

def test_multiplication():
    # Test simple multiplication
    result = 3 * 3
    assert result == 9  # Check if 3 * 3 equals 9

def test_division():
    # Test simple division
    result = 8 / 2
    assert result == 4  # Check if 8 / 2 equals 4
